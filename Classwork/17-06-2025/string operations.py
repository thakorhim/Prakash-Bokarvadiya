#string operations
s = "python programming"
print(s)#original string
print(s.capitalize()) #capitalize
print(s.upper())   #upper
print(s.lower())   #lower
print(s.swapcase()) #swapcase
print(s.index("t"))   #index
print(s.count("p"))   #count
print(s.title())     #title
print(s.isalpha()) #isalpha 
print(len(s))      #length 
print(s.center(26,"*"))#center

